    public void PokemonData(Monster monster) {
		
		String pokemontarget = monster.name.getIndex();
		
        String target = "https://pokeapi.co/api/v2/pokemon/" + pokemontarget;

        HttpClient cliente = HttpClient.newBuilder()
                .followRedirects(HttpClient.Redirect.NORMAL)
                .build();

        HttpRequest request = HttpRequest.newBuilder()
                .uri(URI.create(target))
                .header("User-Agent", "Mozilla/5.0 (Windows NT 10.0; Win64; x64)")
                .header("Accept", "application/json")
                .GET()
                .build();

        try {
            HttpResponse<String> anwser = cliente.send(request, HttpResponse.BodyHandlers.ofString());
            
            if (anwser.statusCode() == 200) {
                
                String data = anwser.body();
                
                
                String hp = extractData(data, "(?s)\"base_stat\"\\s*:\\s*(\\d+).{0,100}?\"name\"\\s*:\\s*\"hp\"");
                String attack = extractData(data, "(?s)\"base_stat\"\\s*:\\s*(\\d+).{0,100}?\"name\"\\s*:\\s*\"attack\"");
                String defence = extractData(data, "(?s)\"base_stat\"\\s*:\\s*(\\d+).{0,100}?\"name\"\\s*:\\s*\"defense\"");
                String spAttack = extractData(data, "(?s)\"base_stat\"\\s*:\\s*(\\d+).{0,100}?\"name\"\\s*:\\s*\"special-attack\"");
                String spDefence = extractData(data, "(?s)\"base_stat\"\\s*:\\s*(\\d+).{0,100}?\"name\"\\s*:\\s*\"special-defense\"");
                String speed = extractData(data, "(?s)\"base_stat\"\\s*:\\s*(\\d+).{0,100}?\"name\"\\s*:\\s*\"speed\"");
                
                String stype1 = extractData(data, "(?s)\"slot\"\\s*:\\s*1.{0,60}?\"type\"\\s*:\\s*\\{\\s*\"name\"\\s*:\\s*\"([^\"]+)\"");
                String stype2 = extractData(data, "(?s)\"slot\"\\s*:\\s*2.{0,60}?\"type\"\\s*:\\s*\\{\\s*\"name\"\\s*:\\s*\"([^\"]+)\"");

                if (hp != null) monster.Health = Integer.parseInt(hp);
                if (attack != null) monster.Attack = Integer.parseInt(attack);
                if (defence != null) monster.Defence = Integer.parseInt(defence);
                if (spAttack != null) monster.SpecialAttack = Integer.parseInt(spAttack);
                if (spDefence != null) monster.SpecialDefence = Integer.parseInt(spDefence);
                if (speed != null) monster.Speed = Integer.parseInt(speed);
                
                if (stype1 != null) {
                    monster.type1 = Type.valueOf(stype1.toUpperCase()); 
                }

                if (stype2 != null) {
                    monster.type2 = Type.valueOf(stype2.toUpperCase());
                } else {
                    monster.type2 = null; 
                }

                
  
            } else {
                System.out.println("Status: " + anwser.statusCode());
                System.out.println("Headers: " + anwser.headers().map());
            }
        } catch (Exception e) {
            System.out.println("Fatal Error: " + e.getMessage());
        }
	}
	
	private static String extractData(String json, String regexPattern) {
        Pattern pattern = Pattern.compile(regexPattern);
        Matcher matcher = pattern.matcher(json);
        if (matcher.find()) {
            return matcher.group(1);
        }
        return null;
    }
