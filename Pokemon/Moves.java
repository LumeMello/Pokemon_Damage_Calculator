public void MoveData(Monster monster) {
		
		String movetarget = monster.move.getIndex();
		
        String target = "https://pokeapi.co/api/v2/move/" + movetarget;

        HttpClient cliente = HttpClient.newBuilder()
                .followRedirects(HttpClient.Redirect.NORMAL)
                .build();

        HttpRequest request = HttpRequest.newBuilder()
                .uri(URI.create(target))
                .header("User-Agent", "Mozilla/5.0 (Windows NT 10.0; Win64; x64)")
                .header("Accept", "application/json")
                .GET()
                .build();

        try {
            HttpResponse<String> anwser = cliente.send(request, HttpResponse.BodyHandlers.ofString());
            
            if (anwser.statusCode() == 200) {
                
                String data = anwser.body();
                
                
                String spower = extractData(data, "(?s)\"power\"\\s*:\\s*(\\d+)");
                String saccuracy = extractData(data, "(?s)\"accuracy\"\\s*:\\s*(\\d+)");

                
                String scategory = extractData(data, "(?s)\"damage_class\"\\s*:\\s*\\{\\s*\"name\"\\s*:\\s*\"([^\"]+)\"");
                String stype = extractData(data, "(?s)\"type\"\\s*:\\s*\\{\\s*\"name\"\\s*:\\s*\"([^\"]+)\"");
                
                if (spower != null) monster.move.power = Integer.parseInt(spower);
                if (saccuracy != null) monster.move.accuracy = Integer.parseInt(saccuracy);
                
                if (stype != null) {
                    monster.move.type = Type.valueOf(stype.toUpperCase()); 
                }
                if (scategory != null) {
                    monster.move.category = Category.valueOf(scategory.toUpperCase());
                }
                
  
            } else {
                System.out.println("Status: " + anwser.statusCode());
                System.out.println("Headers: " + anwser.headers().map());
            }
        } catch (Exception e) {
            System.out.println("Fatal Error: " + e.getMessage());
        }
	}
	
	private static String extractData(String json, String regexPattern) {
        Pattern pattern = Pattern.compile(regexPattern);
        Matcher matcher = pattern.matcher(json);
        if (matcher.find()) {
            return matcher.group(1);
        }
        return null;
    }
